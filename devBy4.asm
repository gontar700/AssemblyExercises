MODEL small
STACK 100h
DATASEG
    ; --- ????? ?????? ?? ?????? (????? ???????-??????) ---
    x     dw 150
    y     dw 90
    color db 4       ; ????
    len   dw 20      ; ???? ?????? (20x20 ???????)
CODESEG


setDS macro
    mov ax, @data
    mov ds, ax
endm

graphicMode macro
    mov ax, 13h
    int 10h
endm

textMode macro
    mov ax, 3h
    int 10h
endm

quitGame macro
    mov ax, 4c00h   
    int 21h
endm

drawPixel proc
    push ax bx cx dx
    mov bh, 0h
    mov cx, [x]
    mov dx, [y]
    mov al, [color]
    mov ah, 0ch
    int 10h
    pop dx cx bx ax
    ret
drawPixel endp

drawLine proc
    push cx          
    push [x]         ; ???? ?? ?-X ?????? ?????? ?????
    
    mov cx, [len]    
pixelLoop:
    call drawPixel   
    inc [x]          
    loop pixelLoop   
    
    pop [x]          ; ????? ?? ?-X ????? ????? ?????
    pop cx           
    ret
drawLine endp

drawSquare proc
    push cx
    push [y]         ; ???? ?? ?-Y ?????? ???? ??????
    
    mov cx, [len]
lineloop:
    call drawLine
    inc [y]          ; ???? ???? ??? ????
    loop lineloop
    
    pop [y]          ; ????? ?? ?-Y ????? ????? ??????
    pop cx
    ret
drawSquare endp


start:
    setDS
    graphicMode      ; ????? ??????? ???? ????

game_loop:
    ; --- ??? 1: ????? ???? ---
    ; ????? ???? ?? ??? 13h ???? ?? ?????? ????? ?????? "???" ?? ?????
    graphicMode      

    ; --- ??? 2: ???? ?????? ?????? ?????? ??? ---
    call drawSquare  

    ; --- ??? 3: ????? ?????? (Delay) ---
    mov cx, 0ffffh
delay_loop:
    nop
    loop delay_loop

    ; --- ??? 4: ????? ????? (??? ???? ????) ---
    mov ah, 01h
    int 16h
    
    jnz key_was_pressed  ; ?? ???? ??? -> ???? ????? ????? ????
    jmp game_loop        ; ?? ?? ???? ??? -> ???? ???? ?????? ????? ????

key_was_pressed:
    ; ????? ???? ?????? ????? ??????
    mov ah, 00h
    int 16h

    ; --- ??? 5: ????? ???? ?????? ?????? ??????? ---
    cmp al, 'a'
    je move_left
    cmp al, 'd'
    je move_right
    cmp al, 'w'
    je move_up
    cmp al, 's'
    je move_down
    cmp ah, 48h
    je move_up
    cmp ah, 50h
    je move_down
    cmp ah, 4Bh
    je move_left
    cmp ah, 4Dh
    je move_right
    cmp al, 27d         ; ??? Esc (??? ???? 27) ????? ?????
    je exit_program
    jmp game_loop       ; ?? ???? ??? ?? ????, ???? ???? ??????

; --- ?????? ?? ????? (???? 5 ??????? ??? ?????) ---
move_left:
    sub [x], 5
    jmp game_loop

move_right:
    add [x], 5
    jmp game_loop

move_up:
    sub [y], 5
    jmp game_loop

move_down:
    add [y], 5
    jmp game_loop

; --- ????? ?????? ???????? ---
exit_program:
    textMode         
    quitGame        

END start